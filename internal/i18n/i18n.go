func Set(lang string) {
	if _, ok := strings[lang]; ok {
		current = lang
	}
}

func Get() string {
	return current
}

func T(key string) string {
	if m, ok := strings[current]; ok {
		if v, ok := m[key]; ok {
			return v
		}
	}
	if v, ok := strings["en"][key]; ok {
		return v
	}
	return key
}

